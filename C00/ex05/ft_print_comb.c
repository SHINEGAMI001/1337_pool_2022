#include <unistd.h>

void	ft_print_comb(void)
{
	int	n;
	int	a;
	int	b;

	n = '0';
	while (n <= '7')
	{
		a = n + 1;
		while (a <= '8')
		{
			b = a + 1;
			while (b <= '9')
			{
				write(1, &n, 1);
				write(1, &a, 1);
				write(1, &b, 1);
				if (n != '7')
					write(1, ", ", 2);
				b++;
			}	
			a++;
		}
		n++;
	}
}
